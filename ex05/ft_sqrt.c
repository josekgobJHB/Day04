int ft_sqrt(int	nb)
{
				if(nb >= 1)
				{
								int root;

								root = 1;
								while(root * root < nb)
								{
												root++;
								}
								if(root*root == nb)
								{
												return (root);
								}
				}
				else
				{				
								return	(0);
				}
}



								
